#include<iostream>
using namespace std;

int main(){
    // Printing output
    cout<<"Hello World! \n";        // "<<" is Insertion operator

    //Taking input from user
    int a;
    cout<<"Enter the value of a: ";
    cin>>a;                         // ">>"is Extraction operator

    cout<<"The value of a is: "<<a<<endl;

    return 0;
}
